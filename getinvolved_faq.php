			<h4>Volunteer FAQ’s<h4>

			<h4>What are the different volunteer opportunities?</h4>
			<p>Pick your passion.  Please contact us and we will find a good fit for your skills. We are always in need of help on the farm, with fundraising and administrative needs, photography, wood-working, or in grant research/writing.</p>

            <h4>What is the work like?</h4>
            <p>Farming is hard work depending on the season, but it can be fun and invigorating and help you feel connected to the earth. You may be preparing beds, planting, harvesting, tending vegetables or cleaning up the land for 2 to 3 hours, rain or shine. Expect to get your hands and clothes dirty by the end of the morning, and to feel better about skipping your workout at the gym.</p>
            <h4>What do I need to bring?</h4>
            <p>Work gloves, water, sunscreen, and/or rain gear (depending on weather.) We will supply all the tools you need to work in the field. If you have a medical condition such as asthma or anaphylaxis, please bring your inhaler or epi-pen and tell your group leader of your condition before working in the field.</p>
            <h4>What do I wear when working on the land?</h4>
            <p>Long pants and long sleeve shirt are recommended for farm work. Close-toed shoes are required because you may be working with sharp tools. Keep in mind that you will get dirty, and be prepared for warm, cold, or wet weather. Bring extra layers and rain gear so that you are comfortable working in most weather conditions. A box of garbage bags is a good item to bring in case people in your group don't bring rain gear.</p>
            <h4>Do we still work when it rains?</h4>
            <p>Yes, we work when it rains. The field trainer(s) will tell staff and group leaders to stop working when weather conditions are not conducive for fieldwork.</p>
            <h4>What about lunch and water?</h4>
            <p>Feel free to bring your own lunch. Bottled water may be available, although we suggest that you bring some with you.</p> 
            <h4>How old do I have to be to work out on the land?</h4>
            <p>The age requirement for individual volunteers is 17 or older.  High school seniors, who need to complete service hours, are invited to work as an individual volunteer on the land without parental supervision.
            Younger children are welcome to visit the farm with their family, but we are not able to accommodate them during work days on the farm.  We do ask that parents be responsible for the supervision of their children whenever visiting the farm.</p>
            <h4>How large a group can help service the land?</h4>
            <p>Volunteering requests must be received through our website or staff at least 24 hours in advance from the date of your planned service day(s). We can accommodate groups of up to 20 people at a time with proper planning and advance notice.</p>
            <h4>What if I have a physical ailment such as a bad back?</h4>
            <p>Please let us know before the day you volunteer so that we can assign you a task that is accommodating to your physical or medical condition.</p>
            <h4>Do individual volunteers need to complete forms before working on the farm?</h4>
            <p>All individual volunteers must fill out an Emergency Contact and Waiver Form.  They are available at the site on the first day you volunteer.  In case of an emergency while you are on the land, we would like to know who we should contact.</p>
            <p>To volunteer or receive more information, please contact us at <strong>877-771-GIVE</strong> or email <a href="mailto:give@thegroundbreakers.org" target="_blank">give@thegroundbreakers.org</a></p>

